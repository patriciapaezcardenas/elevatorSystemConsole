namespace ElevatorSystem.Domain.Enums
{
    /// <summary>
    /// Represents the possible directions an elevator can move.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Represents the upward direction.
        /// </summary>
        UP = 1,

        /// <summary>
        /// Represents the downward direction.
        /// </summary>
        DOWN = 2,

        /// <summary>
        /// Represents no specific direction.
        /// </summary>
        NONE = 3
    }
}
